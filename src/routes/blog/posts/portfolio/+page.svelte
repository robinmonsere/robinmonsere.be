<h1>The creation of <a href="https://www.robinmonsere.be">robinmonsere.be</a></h1>


<p>This website, the porftolio and blog, was created to showcase my skills in creating a website using modern tools.</p>

<h2>Design</h2>
<p>I started with the design of this website by looking at other popular portfolios, I got most of my inspiration from
    <a href="https://brittanychiang.com/">here</a>.</p>
<p> I used Figma to finalize the design before starting to code.</p>

<h2>Framework</h2>
<p>I wanted to make this site using a framework I had never worked with before, and whilst reading about popular frameworks, I came across Svelte.</p>
<p>Since I first started this project, I began to love Svelte more and more. By now, I have done multiple other projects using it.</p>
<p>I particularly love the file based route system and its great documentation!</p>

<h2>Deployment</h2>
<p>I deployed this site on Combell.</p>

