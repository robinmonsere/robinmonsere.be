<h1>Lieven Schiere and AI</h1>

<p>When Lieven Scheire announced he was doing a tour about AI, I knew I had to see it.</p>
<p>So, on the second of Februari, I went to "De Leest" in Izegem to watch Lieven talk about AI. It was a very insightful talk where I learned more about AI than I first thought I would.</p>

<h2>Pattern Recognition</h2>
<p>Scheire highlighted the paradigm shift from classical programming to pattern recognition, underscoring the transition with examples like Clippy, Microsoft's early attempt at AI assistance. This evolution has paved the way for sophisticated AI applications capable of understanding and assisting users in unprecedented ways. He gave examples of doctors using AI to diagnose certain diseases with AI models, even where the doctors themselves cannot diagnose those diseases.</p>

<h2>Smartphones are stronger than ever</h2>
<p>Drawing attention to the exponential growth in computing power, Scheire astounded us with the revelation that modern smartphones are 100,000 times more powerful than the computers that propelled Apollo 11 to the moon. These big increases in computational power have enabled apps like Photomath and Google Translate, he then proceeded to show us some examples using these apps.</p>

<h2>AI's Diverse Applications</h2>
<p>Scheire showcased a myriad of AI applications, from solving mathematical problems with Photomath to real-time multilingual translation using tools like Google Translate. These examples underscored the versatility and practicality of AI in addressing a wide range of challenges.</p>

<h2>The Black Box</h2>
<p>Scheire delved into the enigmatic nature of AI's self-training processes, highlighting the challenge of comprehending its inner workings as machines learn and evolve autonomously. Understanding these mechanisms is crucial for ensuring transparency and accountability in AI systems.</p>

<h2>Surpassing Human Capabilities</h2>
<p>Scheire showcased instances where AI surpasses human abilities, from mastering complex games like Go and chess to solving intricate biological problems like protein folding. These achievements underscore the transformative potential of AI in various fields.</p>

<h2>Challenges in Training Data</h2>
<p>Addressing a critical issue, Scheire discussed the presence of biases in AI training data, emphasizing the need to mitigate these biases for ethical and accurate AI applications. By addressing bias, we can ensure fairness and inclusivity in AI-driven technologies.</p>


<h2>Mindblowing ChatGPT demonstration</h2>
<p>In a conversation with GPT, Lieven illustrated GPT's remarkable capabilities in natural language processing and interaction. This demonstration underscored the potential of AI in generating human-like text and engaging in meaningful conversations.</p>

<h2>The Secrets of Captcha</h2>
<p>At the end, Scheire unveiled the dual purpose of Captcha, which not only serves as a barrier against robots but also as a tool for training AI. This multifaceted role highlights how multi billion companies are using us to train their LLM's or other AI's .</p>


<h2>Conclusion</h2>
<p>Lieven Scheire's insightful exploration of AI provided a deeper understanding of its evolution, challenges, and transformative potential. As we continue to navigate the frontiers of AI, his guidance serves as a beacon illuminating the path forward. It inspired me to delve deeper into the fascinating world of Artificial Intelligence.</p>

