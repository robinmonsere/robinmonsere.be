<h2>Introduction to INCASO</h2>

<p>Welcome to our blog, where we share our enriching experience participating in the INCASO project during the carnival week in Rijeka, Croatia. Our international group, composed of students from various European countries, collaborated on a project aimed at enhancing the local beach as a tourist destination. This adventure not only immersed us in Rijeka's vibrant culture and carnival festivities but also fostered intercultural dialogue and understanding through workshops, discussions, and community engagement. The project highlighted the city's maritime heritage and its famous February carnival, featuring parades, concerts, and cultural events.</p>

<h2>Arrival and Exploration</h2>

<p>Our journey began on Sunday with a flight from Belgium to Ljubljana, Slovenia, followed by a scenic bus ride to Rijeka. We were warmly welcomed at our hostel, quickly bonding with our international roommates. The following days were packed with activities: exploring Rijeka's festive city center, attending workshops on social innovation and sustainable tourism, and brainstorming ways to revitalize Preluk beach. We delved into Rijeka's cultural heritage with visits to the Children's House and Sugar Refinery Palace, and learned about city culture as a touristic brand, leadership, and sustainable development goals.</p>

<h2>Project Development and Cultural Immersion</h2>

<p>As the week progressed, we honed our project ideas, culminating in presentations to judges on Friday. The experience was both nerve-wracking and rewarding, showcasing our teamwork and innovative solutions. Despite the rigorous schedule, we found time to explore Rijeka's culinary delights and enjoy evenings at local pubs, building lasting friendships. Our trip concluded with a visit to the Uƒçka Nature Park, where we marveled at the multimedia exhibition and breathtaking scenery, reflecting on the importance of nature conservation.</p>

<h2>The Rijeka Carnival Experience</h2>

<p>The highlight of our trip was participating in the Rijeka International Carnival Parade on Sunday. Dressed as characters from The Addams Family, we danced and celebrated alongside the locals, an experience broadcasted on national television. The carnival's lively atmosphere and our interactions with the crowd made for an unforgettable finale to our week in Rijeka. This project not only broadened our cultural horizons but also emphasized the value of international collaboration and community involvement in creating sustainable and attractive tourist destinations.</p>


